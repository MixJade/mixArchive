package org.example;

class ReC {
    private final String oldClass;
    private final String newClass;

    public ReC(String oldClass, String newClass) {
        this.oldClass = oldClass;
        this.newClass = newClass;
    }

    public String getOldClass() {
        return oldClass;
    }

    public String getNewClass() {
        return newClass;
    }
}
